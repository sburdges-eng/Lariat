// scripts/dump-fresh-schema.mjs — Phase C2 read-only schema-baseline helper.
//
// Builds a FRESH temporary SQLite database through the web app's own
// open/migrate path (lib/db.ts :: setDbPathForTest → getDb → initSchema —
// the exact same mechanism tests/js/helpers/beo-fixtures.mjs uses) and
// prints a dump of the result to stdout. NEVER touches data/lariat.db —
// the DB is created under a mkdtemp dir and deleted on exit.
//
// Modes (mutually exclusive):
//   node scripts/dump-fresh-schema.mjs
//     → normalized schema dump. One line per sqlite_master object:
//       `<type>|<name>|<normalized sql>`, sorted by (type, name), with
//       `IF NOT EXISTS` stripped and all whitespace runs collapsed to a
//       single space. sqlite_* internals and the native migrator's
//       `grdb_migrations` bookkeeping table are excluded on both sides.
//       Committed as:
//         LariatNative/Tests/LariatDBTests/Fixtures/web_schema_baseline.sql
//
//   node scripts/dump-fresh-schema.mjs --seeds
//     → deterministic seed-row dump (every table that has rows after a
//       fresh init; timestamp columns excluded). Committed as:
//         LariatNative/Tests/LariatDBTests/Fixtures/web_seed_baseline.txt
//
//   node scripts/dump-fresh-schema.mjs --full
//     → executable SQL (DDL as stored in sqlite_master + INSERTs for all
//       rows) that reconstructs the fresh web-built DB byte-for-byte at
//       the schema level. Used by the native boundary test "running the
//       SchemaMigrator against an ALREADY-migrated web DB is a no-op".
//       Committed as:
//         LariatNative/Tests/LariatDBTests/Fixtures/web_full_dump.sql
//
// The Swift mirror of the normalization lives in
// LariatNative/Sources/LariatDB/SchemaDump.swift — keep the two in sync.

import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { register } from 'node:module';

// Same TS-resolve hook as the tests / sibling importers so we can
// import lib/*.ts modules from a plain .mjs script.
register(new URL('../tests/js/resolver.mjs', import.meta.url));

const MODE = process.argv.includes('--seeds')
  ? 'seeds'
  : process.argv.includes('--full')
    ? 'full'
    : process.argv.includes('--executable')
      ? 'executable'
      : 'schema';

/**
 * Make a stored `sqlite_master.sql` (or INSERT) statement re-entrant so the
 * native SchemaMigrator can replay it against BOTH a fresh DB and an
 * already-migrated web DB without error — matching lib/db.ts initSchema()'s
 * IF-NOT-EXISTS re-entrancy. SQLite strips `IF NOT EXISTS` when it stores DDL,
 * so we re-insert it.
 */
function reentrant(sql) {
  return sql.replace(
    /^CREATE (TABLE|UNIQUE INDEX|INDEX|TRIGGER|VIEW) (?!IF NOT EXISTS)/i,
    'CREATE $1 IF NOT EXISTS ',
  );
}

/** Columns excluded from the --seeds dump (nondeterministic timestamps). */
const NONDETERMINISTIC_COLS = new Set(['created_at', 'updated_at', 'applied_at', 'imported_at']);

/**
 * Normalize one sqlite_master.sql text for comparison:
 * collapse all whitespace runs to a single space, trim, then strip
 * `IF NOT EXISTS ` (SQLite may or may not retain it in sqlite_master
 * depending on object type — stripping makes both sides canonical).
 *
 * MUST match SchemaDump.normalize(_:) in
 * LariatNative/Sources/LariatDB/SchemaDump.swift.
 */
function normalizeSql(sql) {
  return sql
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/IF NOT EXISTS /gi, '');
}

function headerLines(mode) {
  const regen = {
    schema: 'node scripts/dump-fresh-schema.mjs > LariatNative/Tests/LariatDBTests/Fixtures/web_schema_baseline.sql',
    seeds: 'node scripts/dump-fresh-schema.mjs --seeds > LariatNative/Tests/LariatDBTests/Fixtures/web_seed_baseline.txt',
    full: 'node scripts/dump-fresh-schema.mjs --full > LariatNative/Tests/LariatDBTests/Fixtures/web_full_dump.sql',
    executable: 'node scripts/dump-fresh-schema.mjs --executable > LariatNative/Sources/LariatDB/Resources/frozen_schema.sql',
  }[mode];
  return [
    `-- Generated by scripts/dump-fresh-schema.mjs (mode: ${mode}).`,
    '-- Source of truth: lib/db.ts initSchema() replayed against a fresh temp DB.',
    `-- Regenerate with:`,
    `--   ${regen}`,
    '-- Comment lines (--) and blank lines are ignored by the Swift parity tests.',
    '',
  ];
}

const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'lariat-schema-dump-'));
const tmpDbPath = path.join(tmpDir, 'lariat-fresh.db');

let exitCode = 0;
const dbMod = await import('../lib/db.ts');
try {
  dbMod.setDbPathForTest(tmpDbPath);
  const db = dbMod.getDb();

  const objects = db
    .prepare(
      `SELECT type, name, tbl_name, sql FROM sqlite_master
        WHERE sql IS NOT NULL
          AND name NOT LIKE 'sqlite_%'
          AND name <> 'grdb_migrations'`,
    )
    .all();

  const out = headerLines(MODE);

  if (MODE === 'schema') {
    const lines = objects
      .map((o) => `${o.type}|${o.name}|${normalizeSql(o.sql)}`)
      .sort();
    out.push(...lines);
  } else if (MODE === 'seeds') {
    const tables = objects
      .filter((o) => o.type === 'table')
      .map((o) => o.name)
      .sort();
    for (const table of tables) {
      const count = db.prepare(`SELECT COUNT(*) AS c FROM "${table}"`).get().c;
      if (count === 0) continue;
      const cols = db
        .prepare(`PRAGMA table_info("${table}")`)
        .all()
        .map((c) => c.name)
        .filter((c) => !NONDETERMINISTIC_COLS.has(c));
      const rows = db
        .prepare(`SELECT ${cols.map((c) => `"${c}"`).join(', ')} FROM "${table}"`)
        .all();
      const rendered = rows
        .map((r) => cols.map((c) => `${c}=${r[c] === null ? 'NULL' : String(r[c])}`).join('|'))
        .sort();
      for (const line of rendered) out.push(`${table}|${line}`);
    }
  } else {
    // --full / --executable: executable reconstruction. Tables first
    // (sqlite_master forward-FK-safe), then indexes/views/triggers, then data.
    // --executable additionally makes every statement re-entrant (CREATE ...
    // IF NOT EXISTS / INSERT OR IGNORE) so the native SchemaMigrator can replay
    // it against a fresh OR an already-migrated DB — matching initSchema().
    const reentrantMode = MODE === 'executable';
    const quote = (v) => {
      if (v === null) return 'NULL';
      if (typeof v === 'number' || typeof v === 'bigint') return String(v);
      return `'${String(v).replace(/'/g, "''")}'`;
    };
    const ddl = (sql) => (reentrantMode ? reentrant(sql) : sql);
    const insertVerb = reentrantMode ? 'INSERT OR IGNORE INTO' : 'INSERT INTO';
    for (const o of objects.filter((x) => x.type === 'table')) {
      out.push(`${ddl(o.sql)};`);
    }
    for (const o of objects.filter((x) => x.type !== 'table')) {
      out.push(`${ddl(o.sql)};`);
    }
    for (const o of objects.filter((x) => x.type === 'table')) {
      const rows = db.prepare(`SELECT * FROM "${o.name}"`).all();
      for (const r of rows) {
        const cols = Object.keys(r);
        out.push(
          `${insertVerb} "${o.name}" (${cols.map((c) => `"${c}"`).join(', ')}) ` +
            `VALUES (${cols.map((c) => quote(r[c])).join(', ')});`,
        );
      }
    }
  }

  process.stdout.write(out.join('\n') + '\n');
} catch (err) {
  console.error(err);
  exitCode = 1;
} finally {
  dbMod.setDbPathForTest(null);
  try {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  } catch {
    /* ignore */
  }
}
process.exit(exitCode);
