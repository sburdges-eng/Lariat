#!/usr/bin/env node
// Generate a reviewable verification worklist from data/normalized/compliance_rules.jsonl.
//
// Why this exists: Phase 1 left 21 of 46 rows with verification.status === 'unverified'.
// The rows cite source statutes but the language has not been read against current law.
// The integrity rule for this corpus is "never silently auto-correct records" — so this
// script is strictly read-only. It produces a checkbox-style markdown report (or JSON)
// that a human (Sean or his attorney) can sit with and use to flip rows by hand.
//
// What it does NOT do: it does not write back to the JSONL, does not mutate any
// verification.status field, does not call out to any source URL, and does not
// lint/reformat the corpus. Re-runs simply regenerate the unverified subset.
//
// Usage:
//   node scripts/list-unverified-compliance.mjs
//   node scripts/list-unverified-compliance.mjs --json
//   node scripts/list-unverified-compliance.mjs --out docs/compliance-verification-worklist-YYYY-MM-DD.md
//   node scripts/list-unverified-compliance.mjs --jsonl <path>

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DEFAULT_JSONL = path.join(ROOT, 'data', 'normalized', 'compliance_rules.jsonl');

function parseArgs(argv) {
  const args = { json: false, out: null, jsonl: DEFAULT_JSONL };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--json') args.json = true;
    else if (a === '--out') args.out = argv[++i];
    else if (a === '--jsonl') args.jsonl = argv[++i];
    else if (a === '-h' || a === '--help') {
      printHelp();
      process.exit(0);
    } else {
      console.error(`unknown arg: ${a}`);
      process.exit(2);
    }
  }
  return args;
}

function printHelp() {
  process.stdout.write(
    [
      'list-unverified-compliance.mjs',
      '',
      '  --json           Emit machine-readable JSON instead of markdown',
      '  --out <path>     Write to file instead of stdout',
      '  --jsonl <path>   Override input JSONL (default: data/normalized/compliance_rules.jsonl)',
      '  -h, --help       Show this help',
      '',
    ].join('\n'),
  );
}

export function readJsonlRows(jsonlPath) {
  const text = fs.readFileSync(jsonlPath, 'utf-8');
  return text
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean)
    .map((l, i) => {
      try {
        return JSON.parse(l);
      } catch (err) {
        throw new Error(`compliance_rules.jsonl: parse error on line ${i + 1}: ${err.message}`);
      }
    });
}

export function pickUnverified(rows) {
  return rows.filter((r) => r?.verification?.status === 'unverified');
}

export function groupByDomain(rows) {
  const groups = new Map();
  for (const r of rows) {
    const d = r.domain || 'unknown';
    if (!groups.has(d)) groups.set(d, []);
    groups.get(d).push(r);
  }
  // Sort domains alphabetically, rows within a domain by id.
  for (const arr of groups.values()) arr.sort((a, b) => String(a.id).localeCompare(String(b.id)));
  return new Map([...groups.entries()].sort(([a], [b]) => a.localeCompare(b)));
}

function domainHeading(domain) {
  // Convert snake_case -> Title Case for human readability without losing the original token.
  const pretty = domain
    .split('_')
    .map((w) => (w.length ? w[0].toUpperCase() + w.slice(1) : w))
    .join(' ');
  return `${pretty} (\`${domain}\`)`;
}

export function renderMarkdown(unverified, { generatedAt, jsonlPath, total }) {
  const grouped = groupByDomain(unverified);
  const lines = [];
  lines.push('# Compliance Verification Worklist');
  lines.push('');
  lines.push(
    'Rows in `data/normalized/compliance_rules.jsonl` whose `verification.status` is `unverified`.',
  );
  lines.push(
    'Each entry cites a source statute that has not yet been read against current law. Verify the',
  );
  lines.push(
    'language at the cited URL, then flip `verification.status` to `verified` (and update',
  );
  lines.push('`last_verified` to today) directly in the JSONL. This document is regenerated by');
  lines.push('`npm run compliance:worklist` — check items off below as you work.');
  lines.push('');
  lines.push(`- Generated: ${generatedAt}`);
  lines.push(`- Source: \`${path.relative(ROOT, jsonlPath) || jsonlPath}\``);
  lines.push(`- Unverified rows: ${unverified.length} of ${total}`);
  lines.push('');
  lines.push('## Per-domain counts');
  lines.push('');
  for (const [domain, rows] of grouped) {
    lines.push(`- ${domainHeading(domain)}: ${rows.length}`);
  }
  lines.push('');

  for (const [domain, rows] of grouped) {
    lines.push(`## ${domainHeading(domain)}`);
    lines.push('');
    for (const r of rows) {
      const id = r.id || '(no-id)';
      const topic = r.topic || '(no-topic)';
      const sourceTitle = r?.source?.title || '(no source title)';
      const sourceUrl = r?.source?.url || '';
      const effective = r?.source?.effective_date || 'UNKNOWN';
      const summary = (r.plain_language_summary || '').replace(/\s+/g, ' ').trim();
      const summaryLine = summary.length > 280 ? `${summary.slice(0, 277)}...` : summary;

      lines.push(`- [ ] **\`${id}\`** — ${topic}`);
      lines.push(`  - Source: ${sourceTitle}`);
      if (sourceUrl) lines.push(`  - URL: <${sourceUrl}>`);
      lines.push(`  - Effective date: ${effective}`);
      lines.push(`  - Summary: ${summaryLine}`);
      lines.push('');
    }
  }

  return lines.join('\n');
}

export function renderJson(unverified, { generatedAt, jsonlPath, total }) {
  const items = unverified.map((r) => ({
    id: r.id,
    domain: r.domain,
    jurisdiction: r.jurisdiction,
    topic: r.topic,
    plain_language_summary: r.plain_language_summary,
    source: {
      title: r?.source?.title || null,
      url: r?.source?.url || null,
      effective_date: r?.source?.effective_date || null,
    },
  }));
  const payload = {
    generated_at: generatedAt,
    source_jsonl: path.relative(ROOT, jsonlPath) || jsonlPath,
    total_rows: total,
    unverified_count: unverified.length,
    items,
  };
  return JSON.stringify(payload, null, 2) + '\n';
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const rows = readJsonlRows(args.jsonl);
  const unverified = pickUnverified(rows);
  const ctx = {
    generatedAt: new Date().toISOString(),
    jsonlPath: args.jsonl,
    total: rows.length,
  };
  const output = args.json ? renderJson(unverified, ctx) : renderMarkdown(unverified, ctx);
  if (args.out) {
    fs.mkdirSync(path.dirname(args.out), { recursive: true });
    fs.writeFileSync(args.out, output);
    process.stderr.write(
      `wrote ${unverified.length} unverified rows to ${path.relative(ROOT, args.out) || args.out}\n`,
    );
  } else {
    process.stdout.write(output);
  }
}

// Only run if invoked directly (allow imports for tests).
const invokedDirectly = process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1]);
if (invokedDirectly) main();
