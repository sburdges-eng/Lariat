// gen-data.mjs — regenerate the Cockpit's data.js from REAL Lariat data.
//
// Wires the design-atlas cockpit to the actual product:
//   • order guide / prep board  ← the REAL BEO cascade engine
//     (scripts/beo_cascade_cli.py — the same logic ported in-process to
//      LariatNative's BeoCascadeCompute in Native 0.2 L1).
//   • 86 board / "might be out"  ← real menu items from recipes/recipe_index.csv.
//
// The runtime-only board state (station progress, live temps) has no static
// source, so it stays representative; the live web app fills it from
// /api/stations, /api/temp-log, /api/eighty-six.
//
// Run:  node public/design-atlas/larios/ui_kits/cockpit/gen-data.mjs

import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const HERE = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(HERE, '../../../../..') // repo root (owns scripts/, recipes/, menus/)

// ── real BEO cascade for a representative event ────────────────────────────
const event = {
  line_items: [
    { item_name: 'Baked Ziti', quantity: 40 },
    { item_name: 'Battered Fish Taco', quantity: 60 },
    { item_name: 'Caprese Skewers', quantity: 30 },
    { item_name: 'Green Chile Mac Buffet', quantity: 50 },
  ],
  root: ROOT,
  qty_in_yield_units: true,
}
const cascade = JSON.parse(
  execFileSync('python3', [path.join(ROOT, 'scripts/beo_cascade_cli.py')], {
    input: JSON.stringify(event),
    encoding: 'utf8',
  }),
)

// ── real menu items (menus/beo_recipe_map.csv → beo_item, one clean name per row)
function realMenuItems() {
  const lines = readFileSync(path.join(ROOT, 'menus/beo_recipe_map.csv'), 'utf8').split('\n').slice(1)
  const items = new Set()
  for (const line of lines) {
    const name = (line.split(',')[0] || '').trim()
    // real dish names only: drop buffets/add-ons/ingredient rows and CSV noise.
    if (name && !/buffet|add-on|ingredient|^multiple$/i.test(name) && /^[A-Z]/.test(name)) {
      items.add(name)
    }
  }
  return [...items]
}
const menu = realMenuItems()
const pick = (n) => menu.slice(0, n)

// ── shape helpers (deterministic — no randomness) ──────────────────────────
const round1 = (x) => Math.round(x * 10) / 10
const guide = (cascade.order_guide || []).filter((r) => r.total_needed > 0)

// stock: real ingredients + real demand as par; on-hand derived deterministically.
const inventory = guide.slice(0, 8).map((r, i) => {
  const par = round1(r.total_needed)
  const onHand = round1(par * [0.15, 0.9, 0.4, 0.95, 0.12, 0.6, 0.85, 0.3][i % 8])
  const ratio = par > 0 ? onHand / par : 1
  const [tone, status] = ratio < 0.25 ? ['alert', 'Low'] : ratio < 0.5 ? ['warn', 'Watch'] : ['ok', 'OK']
  return { id: i + 1, item: r.ingredient, station: 'Prep', par, onHand, tone, status, unit: r.unit }
})

// prep board: real recipe nodes from the cascade.
const beo = (cascade.prep_demands || []).slice(0, 8).map((p, i) => ({
  id: i + 1,
  item: p.display_name,
  qty: round1(p.qty),
  unit: p.unit,
}))

